import type { ImageMetadata } from "astro"
const images = import.meta.glob<{ default: ImageMetadata }>("/src/assets/images/**/*.{jpeg,jpg,png,webp,avif}", {
  eager: true,
})
export interface Cast {
  name: string
  role: string
  avatar: ImageMetadata
}
export interface TvShow {
  id: string
  title: string
  tagline: string
  description: string
  year: number
  cast: Cast[]
  poster: ImageMetadata
  cover: ImageMetadata
  smallCover: ImageMetadata
  rating: number
  genres: string[]
  creators: string[]
}

function findImage(basePath: string, entityName: string, entityType: string): ImageMetadata {
  const formats = [".webp", ".jpg", ".jpeg", ".png", ".avif"]

  for (const format of formats) {
    const imagePath = basePath + format
    if (images[imagePath]) {
      return images[imagePath].default
    }
  }

  throw new Error(
    `${entityType} image not found for ${entityName}. Make sure the file exists at ${basePath} with one of these extensions: ${formats.join(", ")}.`,
  )
}

function getPoster(id: string): ImageMetadata {
  const basePath = `/src/assets/images/tv-shows/${id}`
  return findImage(basePath, id, "Poster")
}

function getCover(id: string): ImageMetadata {
  const basePath = `/src/assets/images/tv-shows/show-covers/${id}`
  return findImage(basePath, id, "Cover")
}
function getSmallCover(id: string): ImageMetadata {
  const basePath = `/src/assets/images/tv-shows/show-covers/${id}-small`
  return findImage(basePath, id, "Small cover")
}

function getActorAvatar(actorName: string): ImageMetadata {
	const normalizedName = actorName.toLowerCase().replace(/\s+/g, "-")

	const basePath = `/src/assets/images/tv-shows/actors-avatars/${normalizedName}`
	return findImage(basePath, actorName, "Actor avatar")
}

